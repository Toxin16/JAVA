package hellojava;

public class HelloJava {
	public static void main(String[] args) {
		//primitive data type
		byte bt = 127;			//memory 1byte
		short st = 32767;		//memery 2byte
		int i = 2147483647;		//memery 4byte
		long l = 21474836+47;	//memery 4byte
		float f = 1.0f;			//memery 4byte
		double d = 1.0;			//memery 8byte
		boolean b = true;		//memery 1byte
		char c = 'A';			//memery 2byte
		
		System.out.println(bt);
		System.out.println(st);
		System.out.println(i);
		System.out.println(l);
		System.out.println(f);
		System.out.println(d);
		System.out.println(b);
		System.out.println(c);
	}
}
